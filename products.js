const params = new URLSearchParams(window.location.search);
const id = params.get('id');
const product = products[id];

if (product) {
  document.getElementById('product-img').src = product.image;
  document.getElementById('product-name').textContent = product.name;
  document.getElementById('product-price').textContent = product.price;
  document.getElementById('product-description').textContent = product.description;
  document.getElementById('breadcrumb-name').textContent = product.name.toUpperCase();
  document.getElementById('breadcrumb-category').href = product.category + '.html';
  document.getElementById('breadcrumb-category').textContent = product.categoryName;
  document.title = product.name + ' — Majulix';
  document.getElementById('product-video').src = `https://www.youtube.com/embed/${product.videoId}`;

  const detailsDiv = document.getElementById('product-details');
  let detailsHTML = '<h3>PRODUCT DETAILS</h3>';
  for (const [key, value] of Object.entries(product.details)) {
    detailsHTML += `<p><strong>${key}:</strong> ${value}</p>`;
  }
  detailsDiv.innerHTML = detailsHTML;

const thumbnails = document.querySelectorAll('.thumbnail');
  if (product.images) {
    product.images.forEach((img, index) => {
      if (thumbnails[index]) {
        thumbnails[index].src = img;
        thumbnails[index].addEventListener('click', () => {
          document.getElementById('product-img').src = img;
          thumbnails.forEach(t => t.classList.remove('active'));
          thumbnails[index].classList.add('active');
        });
      }
    });
  }

const otherProducts = Object.entries(products).filter(([key]) => key !== id);
const shuffled = otherProducts.sort(() => Math.random() - 0.5);
const exploring = shuffled.slice(0, 4);

exploring.forEach(([key, p], index) => {
  const num = index + 1;
  const imgEl = document.querySelectorAll('.explore-img')[index];
  const nameEl = document.getElementById(`explore-name-${num}`);
  const priceEl = document.getElementById(`explore-price-${num}`);

  if (imgEl) imgEl.src = p.image;
  if (nameEl) nameEl.textContent = p.name;
  if (priceEl) priceEl.textContent = p.price;

  if (imgEl) {
    imgEl.parentElement.parentElement.parentElement.addEventListener('click', () => {
      window.location.href = `product.html?id=${key}`;
    });
  }
});
}